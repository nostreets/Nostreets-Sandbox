    public class Employee : Contact
    {
        public JobRank Rank { get; set; }
        public JobRole Role { get; set; }
        public JobEnding Ending { get; set; }
        public IEnumerable<JobComponent> Components { get; set; }

        public string Title
        {
            get
            {
                string title = null;

                if (Rank != JobRank.None)
                    title += Rank.ToString();

                if (Role != JobRole.None)
                    title += Role.ToString();

                if (Components != null)
                    foreach (JobComponent com in Components)
                        title += com.ToString();


                title += Ending.ToString();

                return title;
            }

        }
    }
